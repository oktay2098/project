     */
    public function index(Request $request)
    {
        $request->validate([
            'text' => 'required',
            'target' => 'required',
        ]);

        $translation = GoogleTranslate::translate($request->text, $request->target);

        return response()->json([
            'translation' => $translation,
        ]);
    }

